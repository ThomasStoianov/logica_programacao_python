baseRetangulo = float(input("Digite a base do retangulo: "))
alturaRetangulo = float(input("Digite a altura do retangulo: "))

areaRetangulo = baseRetangulo * alturaRetangulo
perimetroRetangulo = (baseRetangulo + baseRetangulo) + (alturaRetangulo + alturaRetangulo)

print(f"A area do retangulo é {areaRetangulo}")
print(f"O perimetro do retangulo é {perimetroRetangulo}")